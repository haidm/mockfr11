class Datdt_MegaMenu_Block_Adminhtml_Custom_Edit_Form extends Mage_Adminhtml_Block_Widget_Form
{
    public function __construct()
    {
        parent::__construct();

        $this->setId('editCustomFrm');
        $this->setTitle(Mage::helper('datdt_megamenu')->__('Information'));
    }

    public function _prepareForm()
    {
        $model = Mage::registry('customModel');
        $albums = Mage::getModel('datdt_megamenu/custom')->getCollection();
        $menuSelect = array();

        foreach ($albums as $item)
        {
            $menuSelect[] = array(
                'value' => $item->getId(),
                'label' => $item->getName()
            );
        }

        $form = new Varien_Data_Form(array(
            'id'      => 'edit_form',
            'action'  => $this->getData('action'),
            'method'  => 'post'
        ));

        $fieldset = $form->addFieldset('base', array(
            'ledend' => Mage::helper('datdt_megamenu')->__('Information')
        ));
        $fieldset->addField('name', 'select', array(
            'name'   => 'name',
            'label'  => Mage::helper('datdt_megamenu')->__('Name'),
            'values' => $menuSelect
        ));
        $fieldset->addField('link', 'text', array(
            'name'     => 'link',
            'label'    => Mage::helper('datdt_megamenu')->__('Link'),
            'class'    => 'required-entry',
            'required' => true
        ));

        if ($model->getId())
        {
            $fieldset->addField('id', 'hidden', array(
                'name' => 'id',
            ));
        }

        $form->addValues($model->getData());
        $form->setUseContainer(true);
        $form->setAction($this->getUrl('*/image/save'));

        $this->setForm($form);

        return parent::_prepareForm(); // TODO: Change the autogenerated stub
    }
}